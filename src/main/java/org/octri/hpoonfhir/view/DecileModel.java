package org.octri.hpoonfhir.view;


public class DecileModel {
	
	private Double nth;
	private Double value;
	private String hpo;
	
	public Double getNth() {
		return nth;
	}
	
	public void setNth(Double nth) {
		this.nth = nth;
	}
	
	public Double getValue() {
		return value;
	}
	
	public void setValue(Double value) {
		this.value = value;
	}
	
	public String getHpo() {
		return hpo;
	}
	
	public void setHpo(String hpo) {
		this.hpo = hpo;
	}

}
